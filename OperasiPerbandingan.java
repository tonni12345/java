public class OperasiPerbandingan {
    public static void main(String[] args) {
        // kurang dari
        System.out.println(2<5); // return true
        // lebih dari
        System.out.println(5>2); // return true
        // kurang dari sama dengan
        System.out.println(1<=2); //return true
        // lebih dari sama dengan
        System.out.println(5>=3); // return true
        // sama dengan
        System.out.println(5==5);
        // ! negasi 
        System.out.println(!false);
    }
}
