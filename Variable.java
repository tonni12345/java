public class Variable {
    public static void main(String[] args) {
        // Variable Di Java itu static tidak bisa berubah ubah tipe datanya seperti di bahasa lain
        // contoh
        String nama = "Dono";
        // nama = 10; // akan menimbulkan error int cannot be converted to String // error saat compile time
        System.out.println(nama);
    }
}
